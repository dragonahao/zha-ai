extension LucaColors on Color {
  static Color parse(String hex) {
    // 如果传入的十六进制颜色值不符合要求，返回默认值
    var s = hex;
    if (s.length != 7 || int.tryParse(s.substring(1, 7), radix: 16) == null) {
      s = '#FFFFFF';
    }
    return Color(int.parse(s.substring(1, 7), radix: 16) + 0xFF000000);
  }

  static Color alphaColor(String hex, double alpha) {
    Color c = parse(hex);
    int a = (alpha * 255).toInt();
    Color t = Color.fromARGB(a, c.red, c.green, c.blue);
    return t;
  }

